function listTeams() {
  heroes.forEach((hero) => {
    const div = document.createElement("div");
    div.dataset.summary = hero.summary;
    div.classList.add("squareOpens");

    div.innerHTML = `
    <h3 data-name="${hero.year}">${hero.year}</h3>
    <img data-name="${hero.poster}"src="${hero.poster}"/>

    
    `;

    var heroesDiv = document.getElementById("heroes");

    heroesDiv.appendChild(div);
  });
}

listTeams();

function createModal() {
  var modal = document.getElementById("myModal");
  var btn = document.getElementById("myBtn");
  var span = document.getElementsByClassName("close")[0];

  const squares = document.querySelectorAll(".squareOpens");
  squares.forEach((square) => {
    square.addEventListener(
      "click",
      function (event) {
        const findTeam = heroes.find(
          (p) => p.summary === event.target.dataset.summary
        );

        document.querySelector(
          "#host"
        ).innerHTML = `Host Country: ${findTeam.hostcountry}`;
        document.querySelector(
          "#image"
        ).innerHTML = `<img src="${findTeam.image}"/>`;
        document.querySelector("#final").innerHTML = `Final: ${findTeam.final}`;
        document.querySelector("#score").innerHTML = `Score: ${findTeam.score}`;

        document.querySelector(
          "#goalscorer"
        ).innerHTML = `Top Goalscorer: ${findTeam.topgoalscorer}`;
        document.querySelector(
          "#attendance"
        ).innerHTML = `Attendance for this match: ${findTeam.attendanceinthefinal}`;

        document.querySelector("#summary").innerHTML = findTeam.summary;

        modal.style.display = "block";
      },
      true
    );
  });
  // When the user clicks on <span> (x), close the modal
  span.onclick = function () {
    modal.style.display = "none";
  };
  window.onclick = function (event) {
    if (event.target == modal) {
      modal.style.display = "none";
    }
  };
}
createModal();

